function t(lang, key) {
  const parts = key.split(".");
  let cur = I18N[lang] || I18N.en;
  for (const p of parts) {
    cur = cur?.[p];
  }
  return typeof cur === "string" ? cur : key;
}

function getLang() {
  const stored = localStorage.getItem("sensibyte-lang");
  if (stored === "en" || stored === "nl") return stored;
  return "en";
}

function setLang(lang) {
  if (lang === "en" || lang === "nl") {
    localStorage.setItem("sensibyte-lang", lang);
  }
}

function applyI18n(lang) {
  document.documentElement.lang = lang === "nl" ? "nl" : "en";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!key) return;
    const value = t(lang, key);
    if (el.tagName === "TITLE") {
      document.title = value;
      return;
    }
    // Missing key: keep non-empty HTML fallback instead of showing raw "section.key"
    if (value === key && el.textContent.trim() !== "") {
      return;
    }
    el.textContent = value;
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (!key) return;
    el.innerHTML = t(lang, key);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (!key) return;
    el.setAttribute("placeholder", t(lang, key));
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    if (!key) return;
    el.setAttribute("aria-label", t(lang, key));
  });
  document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
    const key = el.getAttribute("data-i18n-alt");
    if (!key) return;
    el.setAttribute("alt", t(lang, key));
  });

  // Update lang switcher UI
  const toggle = document.querySelector(".lang-toggle");
  if (toggle) toggle.textContent = lang === "nl" ? "🇳🇱" : "🇬🇧";
  document.querySelectorAll(".lang-option").forEach((btn) => {
    const opt = btn.getAttribute("data-lang");
    btn.setAttribute("aria-selected", opt === lang ? "true" : "false");
  });
}
